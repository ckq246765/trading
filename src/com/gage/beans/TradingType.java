package com.gage.beans;

public class TradingType {
	
	private String tradingTypeCode;
	private String tradingTypeName;
	
	public String getTradingTypeCode() {
		return tradingTypeCode;
	}
	public void setTradingTypeCode(String tradingTypeCode) {
		this.tradingTypeCode = tradingTypeCode;
	}
	public String getTradingTypeName() {
		return tradingTypeName;
	}
	public void setTradingTypeName(String tradingTypeName) {
		this.tradingTypeName = tradingTypeName;
	}
	

}
